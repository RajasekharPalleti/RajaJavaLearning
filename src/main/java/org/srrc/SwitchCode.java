package srrc;

public class SwitchCode {
	
	public static void main(String[] args) {
		char character='1';
		switch(character) {
		case 'A': System.out.println("This is C");
				  break;
		case 'a': System.out.println("This is A");
				  break;
		default : System.out.println("This default");
		}
	}

}
